    @classmethod
    def display_task(cls, message):
        lines = message.splitlines()
        width = max(len(s) for s in lines)
        res = ['+' + '-' * width + '+']
        for s in lines:
            res.append('|' + (s + ' ' * width)[:width] + '|')
        res.append('+' + '-' * width + '+')
        print('\n'.join(res))